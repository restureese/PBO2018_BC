    private void tabelBarangMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelBarangMouseClicked
        // TODO add your handling code here:
        int jumlah = evt.getClickCount();
        
        if(jumlah == 2){
            DataBarang barang = new DataBarang();
            int baris = tabelBarang.getSelectedRow();
            String kode = tabelBarang.getValueAt(baris, 0).toString();
            String nama = tabelBarang.getValueAt(baris, 1).toString();
            String jenis = tabelBarang.getValueAt(baris, 2).toString();
            String supplier = tabelBarang.getValueAt(baris, 3).toString();
            String jumlah_barang = tabelBarang.getValueAt(baris, 4).toString();
            String harga = tabelBarang.getValueAt(baris, 5).toString();
            barang.setData(kode, nama, jenis, supplier, jumlah_barang, harga);
            barang.setVisible(true);
        }
    }//GEN-LAST:event_tabelBarangMouseClicked

