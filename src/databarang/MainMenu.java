    protected void showDataBarang(){
        DefaultTableModel tabel = new DefaultTableModel();
        tabel.addColumn("Kode");
        tabel.addColumn("Nama");
        tabel.addColumn("Jenis");
        tabel.addColumn("Supplier");
        tabel.addColumn("Jumlah");
        tabel.addColumn("Harga");
        
        try {
            Connection connect = new Koneksi().getKoneksi();
            String sql = "select * from databarang";
            Statement statement = connect.createStatement();
            ResultSet result = statement.executeQuery(sql);
            
            while(result.next()){
                tabel.addRow(new Object[]{
                    result.getString(1),
                    result.getString(2),
                    result.getString(3),
                    result.getString(4),
                    result.getString(5),
                    result.getString(6)
                });
            }
            tabelBarang.setModel(tabel);
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, ex);
        }
    }

